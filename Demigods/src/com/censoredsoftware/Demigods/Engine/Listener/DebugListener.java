package com.censoredsoftware.Demigods.Engine.Listener;

import org.bukkit.ChatColor;
import org.bukkit.Location;
import org.bukkit.event.EventHandler;
import org.bukkit.event.EventPriority;
import org.bukkit.event.Listener;

import com.censoredsoftware.Demigods.Engine.Event.Altar.AltarCreateEvent;
import com.censoredsoftware.Demigods.Engine.Event.Altar.AltarCreateEvent.AltarCreateCause;
import com.censoredsoftware.Demigods.Engine.Event.Altar.AltarRemoveEvent;
import com.censoredsoftware.Demigods.Engine.Event.Altar.AltarRemoveEvent.AltarRemoveCause;
import com.censoredsoftware.Demigods.Engine.Event.Misc.ChestSpawnEvent;
import com.censoredsoftware.Demigods.Engine.Event.Shrine.ShrineCreateEvent;
import com.censoredsoftware.Demigods.Engine.Event.Shrine.ShrineRemoveEvent;
import com.censoredsoftware.Demigods.Engine.PlayerCharacter.PlayerCharacter;
import com.censoredsoftware.Demigods.Engine.Utility.AdminUtility;

public class DebugListener implements Listener
{
	@EventHandler(priority = EventPriority.MONITOR)
	public void onChestSpawn(ChestSpawnEvent event)
	{
		Location location = event.getLocation();

		// Send debug
		AdminUtility.sendDebug(ChatColor.RED + "Chest generated at " + ChatColor.GRAY + "(" + location.getWorld().getName() + ") " + location.getX() + ", " + location.getY() + ", " + location.getZ());
	}

	@EventHandler(priority = EventPriority.MONITOR)
	public void onAltarCreation(AltarCreateEvent event)
	{
		Location location = event.getLocation();
		AltarCreateCause cause = event.getCause();

		// Send debug
		AdminUtility.sendDebug(ChatColor.RED + "Altar generated by " + cause.toString() + " at " + ChatColor.GRAY + "(" + location.getWorld().getName() + ") " + location.getX() + ", " + location.getY() + ", " + location.getZ());
	}

	@EventHandler(priority = EventPriority.MONITOR)
	public void onAltarRemove(AltarRemoveEvent event)
	{
		Location location = event.getLocation();
		AltarRemoveCause cause = event.getCause();

		// Send debug
		AdminUtility.sendDebug(ChatColor.RED + "Altar at " + ChatColor.GRAY + "(" + location.getWorld().getName() + ") " + location.getX() + ", " + location.getY() + ", " + location.getZ() + " removed by " + cause.toString() + ".");
	}

	@EventHandler(priority = EventPriority.MONITOR)
	public void onShrineCreation(ShrineCreateEvent event)
	{
		Location location = event.getLocation();
		PlayerCharacter owner = event.getCharacter();

		// Send debug
		AdminUtility.sendDebug(ChatColor.RED + "Shrine created by " + owner.getName() + " (" + owner.getDeity() + ") at: " + ChatColor.GRAY + "(" + location.getWorld().getName() + ") " + location.getX() + ", " + location.getY() + ", " + location.getZ());
	}

	@EventHandler(priority = EventPriority.MONITOR)
	public void onShrineRemove(ShrineRemoveEvent event)
	{
		Location location = event.getLocation();
		PlayerCharacter owner = event.getCharacter();

		// Send debug
		AdminUtility.sendDebug(ChatColor.RED + "Shrine at (" + owner.getDeity() + ") at: " + ChatColor.GRAY + "(" + location.getWorld().getName() + ") " + location.getX() + ", " + location.getY() + ", " + location.getZ() + " removed.");
	}
}
